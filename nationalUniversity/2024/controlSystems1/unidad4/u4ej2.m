clear all; close all; clc;

% Carga de paquete utilizado
pkg load symbolic

% variables
syms s kt real positive;

disp('==========================================================================')
disp('G')
disp('r == (1 + 5t)u')
disp('==========================================================================')

G   = kt*(2*s + 1)/(s*(4*s + 1)*(s + 1)^2)
R   = 1/s + 5/s^2
E   = R/(1+G)
eq1 = limit(simplify(s*E), s, 0) <= .1

kt  = solve(eq1, kt)

disp('======================================================================')
disp('SUCCESS')
