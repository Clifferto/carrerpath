% SISTEMA MASA-RESORTE-AMORTIGUADOR
% ==========================================================================

% variables
syms m b k s X F real

disp('Modelo')
disp('==========================================================================')
disp('m x_pp + b x_p + k x  ==  f')
disp('==========================================================================')
disp('')

eq1 = m*X*s^2 + b*X*s + k*X == F

% X/F
% ==========================================================================
X_vs_F  = factor(solve(eq1,X)/F, s)

% V/F
% ==========================================================================
V_vs_F  = factor(s*X_vs_F, s)

% A/F
% ==========================================================================
A_vs_F  = factor(s^2*X_vs_F, s)

disp('==========================================================================')
disp('Mismo Denominador Porque El Sistema No Cambia')
disp('==========================================================================')
