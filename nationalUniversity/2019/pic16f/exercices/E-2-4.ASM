LIST P=16F887
#include <P16F887.inc>

CBLOCK 0X20
	n1L
	n1H
	n2L
	n2H
	resL
	resH
ENDC

ORG 0 
GOTO setup
ORG 5 
setup	NOP
		BANKSEL OSCCON
		MOVLW B'01101100'
		MOVWF OSCCON
		BANKSEL ANSEL
		CLRF ANSEL
		CLRF ANSELH
		BANKSEL PORTA

main	MOVF n1L,W
		ADDWF n2L,W
		MOVWF resL
		BTFSC STATUS,C
		INCF n1H,F
		MOVF n1H,W
		ADDWF n2H,W
		MOVWF resH		
		GOTO main				
END