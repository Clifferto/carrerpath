errorlevel -203, -205, -302
LIST P=16F887
#include <P16F887.inc>
__CONFIG _CONFIG1,B'0010000011110100'

ORG 0
GOTO setup
ORG 5
setup			CLRF PORTB				;INICIALIZA PUERTOS, BANDERAS 
				CLRF PORTD				;Y VARIABLES
				CLRF TMR0
				CLRF INTCON
				BANKSEL OSCCON			;LFINTOSC, ESTABLE 
				MOVLW B'00001010'		;f=31KHz -> Tcy=129us
				MOVWF OSCCON
				BANKSEL ANSELH			
				CLRF ANSELH				;PORTB DIGITAL
				BANKSEL TRISB
				BCF TRISD,TRISD0		;RD0 ENABLE_OUT Y RD0 INT EXT
				BANKSEL OPTION_REG			
				CLRF OPTION_REG		;PULL-UP EN B, TMR0 (OSC=fcy, Ps=2)
				BANKSEL PORTB
				
main			BTFSC PORTB,RB0
				GOTO $-1
				BSF PORTD,RD0
				BTFSS PORTB,RB0
				GOTO $-1
				BCF PORTD,RD0
				GOTO main
					
END