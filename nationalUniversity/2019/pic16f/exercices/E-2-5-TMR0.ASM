LIST P=16F887
#include <P16F887.inc>

CBLOCK 0X20
	w_temp
	sts_temp
ENDC

ORG 0 
GOTO setup
ORG 4 
GOTO INT
ORG 5 
setup	NOP
		BANKSEL OSCCON
		MOVLW B'01101100'
		MOVWF OSCCON
		BANKSEL ANSEL
		CLRF ANSEL
		CLRF ANSELH
		BANKSEL OPTION_REG
		MOVLW B'11000001'
		MOVWF OPTION_REG
		BANKSEL INTCON
		CLRF INTCON
		BSF INTCON,T0IE
main	NOP
		BANKSEL TMR0
		MOVLW .6
		MOVWF TMR0
		BSF INTCON,GIE
		GOTO $
		GOTO main

INT		MOVWF w_temp
		SWAPF w_temp,F
		SWAPF STATUS,W
		MOVWF sts_temp
		SLEEP
		SWAPF sts_temp,W
		MOVWF STATUS
		SWAPF w_temp,W
		BCF INTCON,T0IF
		RETFIE
END