LIST P=16F887
#include <P16F887.inc>

CBLOCK 0X20
	X
	K
ENDC

ORG 0 
GOTO setup
ORG 5 
setup	NOP
		BANKSEL OSCCON
		MOVLW B'01101100'
		MOVWF OSCCON
		BANKSEL ANSEL
		CLRF ANSEL
		CLRF ANSELH
		BANKSEL PORTA

main	CLRF X
		CLRF K
		BSF X,7
		MOVLW .3
		MOVWF K
ret		DECFSZ X
		GOTO ret
		BSF X,7
		DECFSZ K
		GOTO ret
		NOP	
		GOTO main				
END