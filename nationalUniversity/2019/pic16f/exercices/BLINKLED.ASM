errorlevel -203, -205, -302
LIST P=16F887
#include <P16F887.inc>
;POWER-UP-TIMER ON, WATCH-DOG OFF,OSC XT CRYSTAL 4MHz
__CONFIG _CONFIG1,B'0010000011100001'

#define LED_on BSF PORTC,RC1
#define LED_off BCF PORTC,RC1

ORG 0
GOTO setup

ORG 5

setup			CLRF PORTC				;INICIALIZA PORTC
				BANKSEL TRISC
				BCF TRISC,TRISC1		;RC1 AL LED
				BANKSEL TMR2
				CLRF PIR1				;INICIALIZA BANDERA DEL TMR2
				CLRF TMR2				;INICIALIZA TMR2
				CLRF T2CON				;PreS = 16, PosS = 1
				COMF T2CON,F
				BSF T2CON,TMR2ON		;TMR2 COMIENZA

main			LED_on
				BTFSS PIR1,TMR2IF
				GOTO $-1
				LED_off
				BCF PIR1,TMR2IF
				BTFSS PIR1,TMR2IF
				GOTO $-1
				BCF PIR1,TMR2IF
				GOTO main			
				
END